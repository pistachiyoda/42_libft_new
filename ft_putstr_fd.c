#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (s == NULL)
		return ;
	while (1)
	{
		if (*s == '\0')
			return ;
		ft_putchar_fd(*s, fd);
		s++;
	}
}
